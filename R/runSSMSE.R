#' run an MSE using SS OMs
#'
#' High level function to run a management strategy evaluation using Stock
#' Synthesis as the Operating model(s)
#' @param scen_name_vec A vector containing names of the scenarios. The each
#'  string will be a directory containing all the model runs for a scenario.s
#' @param out_dir_scen_vec The directory to which to write output. IF NULL, will
#'  default to the working directory.
#' @param iter_vec The number of iterations per scenario. A vector of integers
#'  in the same order as scen_name_vec.
#' @param OM_name_vec Name of a valid Stock Synthesis stock assessment model from
#'   which to create the OM. Currently, only allows models in the package, so
#'   valid inputs are: \code{"cod"}.
#' @param OM_in_dir_vec Vector of relative or absolute paths to the operating
#'  model, if using a model outside of the SSMSE package.
#' @param EM_name_vec Should be NULL unless \code{MS = "EM"}. Name of a valid Stock
#'   Synthesis stock assessment model to use as an EM. If the value of EM_name
#'   is NULL and  \code{MS = "EM"}, then SSMSE will look for the estimation model
#'   in the path specified in EM_in_dir. valid inputs for EM_name are: \code{"cod"}
#'    or \code{NULL}.
#' @param EM_in_dir_vec Relative or absolute path to the estimation model, if using
#'   model outside of the SSMSE package. Note that this value should be NULL if
#'   \code{MS} has a value other than "EM".
#' @param run_EM_last_yr Should the EM run in the last year if the last year
#'  is an assessment year? TRUE/FALSE option, so the same for all scenarios and
#'  iterations. Defaults to FALSE.
#' @param MS_vec Vector of management strategies. Current options are:
#'   \code{"last_yr_catch"} which uses the previous year's catch;
#'   \code{"no_catch"} which uses 0 catch; \code{"EM"} which uses an stock
#'   synthesis model as the estimation method and the management strategy as
#'   defined in the forecast file of the stock synthesis estimation method;
#'   \code{"Interim"} to modify catch based on survey predictions between
#'   assessments. Users can also specify their own managment strategies as an
#'   function. To use the function, it must be available in the global
#'   enviroment and specified by name in MS. For example, if the function is
#'   called "my_ms" then the user should make it available in the global
#'   environment and specify "my_ms" as a component of MS_vec.
#' @param use_SS_boot_vec Should a bootstrapped data set generated by SS be used?
#'   Defaults to TRUE.
#' @param nyrs_vec Number of years beyond the years included in the OM to run the
#'   MSE. A single integer value.
#' @param nyrs_assess_vec The number of years between assessments. E.g., if an
#'  assessment is conducted every 3 years, put 3 here. A single integer value.
#'  (NOTE: This could be made more flexible by instead reading in a vector of
#'  assessment years, so users could specify irregular numbers of yrs between
#'  assessments.)
#' @param scope Assign the scope of random deviations for rec devs and implementation error.
#' single integer value input where 1=values are identical across all scenarios and iterations
#' (if rec_dev_pattern=user input a vector of length nyrs_vec), 2 (default)=values are random
#' across iterations but the same iterations are used across scenarios
#' (if rec_dev_pattern=user input a matrix with ncols=nyrs_vec and nrows=number of iterations),
#' 3=values are random across all iterations and scenarios (if rec_dev_pattern=user input
#' a matrix with ncols=nyrs_vec and nrows=(number of iterations)*(number of scenarios)).
#' @param rec_dev_pattern Parameter to specify future rec devs. Input options include:
#' 1) "none" to set all future devs to zero (default). 2) "rand" automatically assign normally
#' distributed random recruitment deviations. Input a vector of two values to rec_dev_pars
#' to specify the max number of years over which the sum of rec_devs can diverge from
#' zero and a scalar multiplyer of standard deviation relative to the historic OM. if
#' rec_dev_pars=NULL defaults to c(nyrs_assess, 1). 3) "AutoCorr_rand" automatically calculates
#' random auto-correlated rec-devs based on the distribution of historic deviations. Input a vector of two values to rec_dev_pars
#' to specify the max number of years over which the sum of rec_devs can diverge from
#' zero and a scalar multiplyer of standard deviation relative to the historic OM. if
#' rec_dev_pars=NULL defaults to c(nyrs_assess, 1). 4) "AutoCorr_Spec"
#' generates auto-correlated recruitment deviations from an MA time-series model with user specified
#' parameters. 5) "user" applys a user input vector or matrix of recruitement deviations of
#' length equal nyrs input the vector/matrix to rec_dev-pars.
#' @param rec_dev_pars Input the required parameters as specified by rec_dev_pattern choice.
#' @param impl_error_pattern Parameter to specify future implementation error. Input options include:
#' 1) "none" to set all future catches equal to expected (default). 2) "rand" automatically assign
#' achieved catch relative to expected catch as log normally distributed. Input
#' a vector of ((nseas x nfleet x 2)+ 1) values to impl_error_pars to specify the max number
#' of years over which the achieved mean catch can diverge from the mean specified, the mean
#' (default 1 such that achieved=expected), and the standard devation. If impl_error__pars=NULL defaults to
#' c(nyrs_assess, rep(1,(nfleetxnseas)), rep(0,(nfleetxnseas))). 3) "user" applys a user input vector
#' or matrix of implementation errors of length/columns equal to nyrs x nseas x Nfleets
#' and rows based on assigned scope. Input the vector/matrix to impl_error_pars.
#' @param impl_error_pars Input the required parameters as specified by impl_error_pattern choice.
#' @param sample_struct_list A optional list of lists including which years, seasons,
#'  and fleets should be  added from the OM into the EM for different types of
#'  data. If NULL, the data structure will try to be infered from the pattern
#'  found for each of the datatypes within the EM datafiles. Include this
#'  strucutre for the number of years to extend the model out.
#' @param sample_struct_hist_list An optional list of lists including which years should be
#'  sampled for the historical period for the data generated from the OM. If 
#'  this is left as NULL, then the same sampling scheme will be used as in the
#'  OM's data file. If it is not NULL, then each year 
#' @param future_om_list An optional list of lists including changes that should
#'  be made after the end year of the input model. Each first level list element
#'  outlines 1 change to be made to the operating model. To see an example, try
#'  running \code{\link{create_future_om_list}}. Defaults to NULL, which implies
#'  that the model will be extended forward in time assuming the original model
#'  structure.
#' @param interim_struct_list A optional list of parameters to control an interim assessment
#'  with an example structure below, where Beta=a positive value that is inversely proportional to risk,
#'  MA_years= the number of years to average index observations of when calculating deviations,
#'  assess_freq=the number of years between full assessments during with an interim assessment will happen
#'  every year, and Index_weights is a vector of length n indexes that weights all indexes for multi index
#'  inference.
#'  interim_struct_list<-list(Beta=1,MA_years=3,assess_freq=5,Index_weights=rep(1,max(ref_index[,3])))
#' @param seed Input a fixed seed to replicate previous simulation runs. seed can be a single value
#' for a global seed, n_scenarios+1 length vector for scenario specific and a global seed,
#' n_iterations+n_scenarios+1 length vector for iteration scenario and global seeds. Can also be a list
#' object with a single value under seed[["global"]], a vector under seed[["scenario"]], and a multiple vectors
#' for iteration specific seeds under seed[["iter"]][[1:n_scenarios]].
#' @param run_parallel Option to use parallel processing. Defaults to FALSE.
#' @param n_cores How many cores to use if running in parallel. If is NULL,
#'  defaults to n_cores available - 1 (also capped at n_cores available - 1)
#' @template verbose
#' @export
#' @import parallel
#' @author Kathryn Doering & Nathan Vaughan
#' @examples
#'
#' \dontrun{
#' my_dir <- file.path(tempdir(), "ex-run_SSMSE")
#' dir.create(my_dir)
#' # For the EM, use the specified data structure
#' my_sample_struct_list <- list(
#'   NULL,
#'   list(
#'     catch = data.frame(
#'       Yr = 101:106,
#'       Seas = 1,
#'       FltSvy = 1,
#'       SE = 0.05
#'     ),
#'     CPUE = data.frame(
#'       Yr = c(102, 105),
#'       Seas = 7,
#'       FltSvy = 2,
#'       SE = 0.01
#'     ),
#'     lencomp = data.frame(
#'       Yr = c(102, 105), Seas = 1,
#'       FltSvy = 1, Sex = 0,
#'       Part = 0, Nsamp = 100
#'     ),
#'   )
#' )
#' # Use the default parameter values, except for the once specified.
#' # Note that the scen_list, either specified or internally created in the
#' # function is returned.
#' input_list <- run_SSMSE(
#'   scen_name_vec = c("scen_1", "scen_2"),
#'   out_dir_scen_vec = my_dir,
#'   iter_vec = c(2, 2),
#'   OM_name_vec = c("cod", "cod"),
#'   OM_in_dir_vec = NULL,
#'   EM_name_vec = c(NA, "cod"),
#'   EM_in_dir_vec = NULL,
#'   MS_vec = c("no_catch", "EM"),
#'   use_SS_boot_vec = TRUE,
#'   nyrs_vec = 6,
#'   nyrs_assess_vec = 3,
#'   scope = c("2", "1", "3"),
#'   rec_dev_pattern = c(
#'     "none", "rand", "AutoCorr_rand",
#'     "AutoCorr_Spec", "vector"
#'   ),
#'   rec_dev_pars = NULL,
#'   impl_error_pattern = c("none", "rand", "user"),
#'   impl_error_pars = NULL,
#'   verbose = FALSE,
#'   seed = NULL,
#'   sample_struct_list = my_sample_struct_list
#' )
#' unlink(my_dir, recursive = TRUE)
#' }
run_SSMSE <- function(scen_name_vec,
                      out_dir_scen_vec = NULL,
                      iter_vec,
                      OM_name_vec = NULL,
                      OM_in_dir_vec = NULL,
                      EM_name_vec = NULL,
                      EM_in_dir_vec = NULL,
                      run_EM_last_yr = FALSE,
                      MS_vec = c("EM", "no_catch", "Interim"),
                      use_SS_boot_vec = TRUE,
                      nyrs_vec,
                      nyrs_assess_vec,
                      scope = c("2", "1", "3"),
                      rec_dev_pattern = c(
                        "none", "rand", "AutoCorr_rand",
                        "AutoCorr_Spec", "vector"
                      ),
                      rec_dev_pars = NULL,
                      impl_error_pattern = c("none", "rand", "user"),
                      impl_error_pars = NULL,
                      sample_struct_list = NULL,
                      future_om_list = NULL,
                      sample_struct_hist_list = NULL,
                      interim_struct_list = NULL,
                      verbose = FALSE,
                      seed = NULL,
                      run_parallel = FALSE,
                      n_cores = NULL) {
  # input checks
  scope <- match.arg(as.character(scope), choices = c("2", "1", "3"))
  rec_dev_pattern <- match.arg(rec_dev_pattern,
    choices = c(
      "none", "rand", "AutoCorr_rand",
      "AutoCorr_Spec", "vector"
    )
  )
  impl_error_pattern <- match.arg(impl_error_pattern,
    choices = c("none", "rand", "user")
  )
  if (!all(MS_vec %in% c("EM", "no_catch", "Interim"))) {
    invalid_MS <- MS_vec[unlist(lapply(MS_vec, function(x) !exists(x)))]
    invalid_MS <- invalid_MS[!invalid_MS %in% c("EM", "no_catch", "Interim")]
    if (length(invalid_MS) > 0) {
      stop(
        "Invalid management strategies in MS_vec: ",
        paste0(invalid_MS, collapse = ", "), ". Please check your function",
        " names and make sure they are all available in the global ",
        "environment, if not built into SSMSE."
      )
    }
  }
  check_future_om_list(future_om_list = future_om_list)
  # Note that all input checks are done in the check_scen_list function.
  # construct scen_list from other parameters.
  scen_list <- create_scen_list(
    scen_name_vec = scen_name_vec,
    out_dir_scen_vec = out_dir_scen_vec,
    iter_vec = iter_vec,
    OM_name_vec = OM_name_vec,
    OM_in_dir_vec = OM_in_dir_vec,
    EM_name_vec = EM_name_vec,
    EM_in_dir_vec = EM_in_dir_vec,
    MS_vec = MS_vec,
    use_SS_boot_vec = use_SS_boot_vec,
    nyrs_vec = nyrs_vec,
    nyrs_assess_vec = nyrs_assess_vec,
    sample_struct_list = sample_struct_list,
    sample_struct_hist_list = sample_struct_hist_list,
    interim_struct_list = interim_struct_list
  )
  # check list and change if need to duplicate values.
  scen_list <- check_scen_list(scen_list, verbose = verbose)
  # First reset the R random seed
  set.seed(seed = NULL)
  # Now set the global, scenario, and iteration seeds that will be used as needed

  seed <- set_MSE_seeds(
    seed = seed,
    iter_vec = unlist(lapply(scen_list, function(scen) scen["iter"]))
  )
  # Get directory of base OM files for each scenario as they may be different
  rec_stddev <- rep(0, length(scen_list))
  n_impl_error_groups <- rep(0, length(scen_list))
  rec_autoCorr <- vector(mode = "list", length = length(scen_list))
  for (i in 1:length(scen_list)) {
    tmp_scen_list <- scen_list[[i]]
    if (is.null(tmp_scen_list[["OM_in_dir"]])) {
      OM_dir <- locate_in_dirs(OM_name = tmp_scen_list[["OM_name"]])
    } else {
      OM_dir <- locate_in_dirs(OM_in_dir = tmp_scen_list[["OM_in_dir"]])
    }
    # Read in starter file
    start <- r4ss::SS_readstarter(file.path(OM_dir, "starter.ss"),
      verbose = FALSE
    )
    # Read in data file
    dat <- r4ss::SS_readdat(file.path(OM_dir, start[["datfile"]]),
      section = 1,
      verbose = FALSE
    )
    # Read in control file
    ctl <- r4ss::SS_readctl(
      file = file.path(OM_dir, start[["ctlfile"]]),
      use_datlist = TRUE, datlist = dat,
      verbose = FALSE
    )
    # Read in parameter file
    parlist <- r4ss::SS_readpar_3.30(
      parfile = file.path(OM_dir, "ss.par"),
      datsource = dat, ctlsource = ctl,
      verbose = FALSE
    )

    # Calculate the standard deviation and autocorrelation of historic recruitment deviations
    rec_dev_comb <- rbind(parlist[["recdev1"]], parlist[["recdev2"]])
    rec_stddev[i] <- stats::sd(rec_dev_comb[, 2])
    n_impl_error_groups[i] <- dat[["nseas"]] * dat[["Nfleet"]]

    if (rec_dev_pattern == "AutoCorr_rand" | rec_dev_pattern == "AutoCorr_Spec") {
      rec_autoCorr[[i]] <- stats::arima(x = rec_dev_comb[, 2], order = c(0, 0, 4))
    }
  }

  if (is.null(rec_dev_pars)) {
    # to do: make this a better default value.
    rec_dev_pars <- c(ceiling(mean(nyrs_vec)), 1)
  }

  # make sure values are the correct length
  nyrs_vec <- unlist(lapply(scen_list, function(scen) scen["nyrs"]))
  nyrs_assess_vec <- unlist(lapply(scen_list, function(scen) scen["nyrs_assess"]))
  iter_vec <- unlist(lapply(scen_list, function(scen) scen["iter"]))

  rec_dev_list <- build_rec_devs(yrs = nyrs_vec, scope = scope, rec_dev_pattern = rec_dev_pattern, rec_dev_pars = rec_dev_pars, stddev = rec_stddev, iter_vec = iter_vec, rec_autoCorr = rec_autoCorr, seed = seed)



  impl_error <- build_impl_error(yrs = nyrs_vec, nyrs_assess = nyrs_assess_vec, n_impl_error_groups = n_impl_error_groups, scope = scope, impl_error_pattern = impl_error_pattern, impl_error_pars = impl_error_pars, n_scenarios = length(scen_list), iter_vec = iter_vec, seed = seed)
  # add recdevs, impl_err, and seed to scen_list
  for (i in seq_along(scen_list)) {
    scen_list[[i]][["rec_devs"]] <- rec_dev_list[[i]]
    scen_list[[i]][["impl_error"]] <- impl_error[[i]]
    scen_seed <- vector(mode = "list", length = 3)
    names(scen_seed) <- c("global", "scenario", "iter")
    scen_seed[["global"]] <- seed[["global"]]
    scen_seed[["scenario"]] <- seed[["scenario"]][i]
    scen_seed[["iter"]] <- seed[["iter"]][[i]]
    scen_list[[i]][["scen_seed"]] <- scen_seed
  }

  if (run_parallel) {
    if (!requireNamespace("doParallel", quietly = TRUE)) {
      stop("Package \"doParallel\" needed if run_parallel = TRUE. Please install it.",
        call. = FALSE
      )
    }
    if (!requireNamespace("foreach", quietly = TRUE)) {
      stop("Package \"foreach\" needed if run_parallel = TRUE. Please install it.",
        call. = FALSE
      )
    }
    if (!is.null(n_cores)) {
      n_cores <- min(max(n_cores, 1), (detectCores() - 1))
      cl <- parallel::makeCluster((n_cores))
      doParallel::registerDoParallel(cl, cores = (n_cores))
    } else {
      cl <- parallel::makeCluster((detectCores() - 1))
      doParallel::registerDoParallel(cl, cores = (detectCores() - 1))
    }
  }
  # pass each scenario to run
  for (i in seq_along(scen_list)) {
    tmp_scen <- scen_list[[i]]
    # run for each scenario
    return_df <- run_SSMSE_scen(
      scen_name = names(scen_list)[i],
      out_dir_scen = tmp_scen[["out_dir_scen"]],
      iter = tmp_scen[["iter"]],
      OM_name = tmp_scen[["OM_name"]],
      OM_in_dir = tmp_scen[["OM_in_dir"]],
      EM_name = tmp_scen[["EM_name"]],
      EM_in_dir = tmp_scen[["EM_in_dir"]],
      MS = tmp_scen[["MS"]],
      use_SS_boot = tmp_scen[["use_SS_boot"]],
      nyrs = tmp_scen[["nyrs"]],
      nyrs_assess = tmp_scen[["nyrs_assess"]],
      rec_devs_scen = tmp_scen[["rec_devs"]],
      impl_error = tmp_scen[["impl_error"]],
      scen_seed = tmp_scen[["scen_seed"]],
      sample_struct = tmp_scen[["sample_struct"]],
      future_om_list = future_om_list,
      sample_struct_hist = tmp_scen[["sample_struct_hist"]],
      interim_struct = tmp_scen[["interim_struct"]],
      run_EM_last_yr = run_EM_last_yr,
      verbose = verbose,
      run_parallel = run_parallel,
      n_cores = n_cores
    )
    scen_list[[i]][["errored_iterations"]] <- return_df
  }
  if (run_parallel) {
    parallel::stopCluster(cl)
    doParallel::stopImplicitCluster()
  }
  message("Completed all SSMSE scenarios")
  invisible(scen_list)
}

#' Run an MSE scenario using SS OM
#'
#' High level function to run 1 scenario (but potentially many iterations) for
#' a management strategy evaluation using Stock Synthesis as the Operating Model
#' @param scen_name Name of the scenario. The directory containing all the model
#'  runs the scenario will be stored within a folder of this name.
#' @param out_dir_scen The directory to which to write output. IF NULL, will
#'  default to the working directory.
#' @param iter The number of iterations for the scenario. A single integer
#'  value.
#' @param OM_name Name of a valid Stock Synthesis stock assessment model from
#'   which to create the OM. Currently, only allows models in the package, so
#'   valid inputs are: \code{"cod"}.
#' @param OM_in_dir Relative or absolute path to the operating model, if using a
#'   model outside of the SSMSE package.
#' @param EM_name Should be NULL unless \code{MS = "EM"}. Name of a valid Stock
#'  Synthesis stock assessment model to use as an EM. If the value of EM_name
#'  is NULL and  \code{MS = "EM"}, then SSMSE will look for the estimation model
#'  in the path specified in EM_in_dir. valid inputs for EM_name are: \code{"cod"}
#'  or \code{NULL}.
#' @param EM_in_dir Relative or absolute path to the estimation model, if using a
#'   model outside of the SSMSE package. Note that this value should be NULL if
#'   \code{MS} has a value other than "EM".
#' @param run_EM_last_yr Should the EM run in the last year if the last year
#'  is an assessment year? TRUE/FALSE option, so the same for all scenarios and
#'  iterations. Defaults to FALSE.
#' @template MS
#' @param use_SS_boot Should a bootstrapped data set generated by SS be used?
#'   Defaults to TRUE.
#' @param nyrs Number of years beyond the years included in the OM to run the
#'   MSE. A single integer value.
#' @param nyrs_assess The number of years between assessments. E.g., if an
#'   assessment is conducted every 3 years, put 3 here. A single integer value.
#'   (NOTE: we could make this more flexible by instead reading in a vector of
#'   assessment years, so users could specify irregular numbers of yrs between
#'   assessments.)
#' @param rec_devs_scen List containing a recruitment deviation vector for each
#'   iteration.
#' @param impl_error List containing an implementation error vector for each
#'   iteration.
#' @param scen_seed List containing fixed seeds for this scenario and its iterations.
#' @param sample_struct A optional list including which years, seasons, and fleets
#'  should be  added from the OM into the EM for different types of data.
#'  If NULL, the data structure will try to be infered from the pattern found
#'  for each of the datatypes within the EM datafiles. Include this strucutre
#'  for the number of years to extend the model out.
#' @param future_om_list An optional list of lists including changes that should
#'  be made after the end year of the input model. Each first level list element
#'  outlines 1 change to be made to the operating model. To see an example, try
#'  running \code{\link{create_future_om_list}}. Defaults to NULL, which implies
#'  that the model will be extended forward in time assuming the original model
#'  structure.
#' @param sample_struct_hist An optional list including which years should be
#'  sampled for the historical period for the data generated from the OM. If 
#'  this is left as NULL, then the same sampling scheme will be used as in the
#'  OM's data file. If it is not NULL, then each year 
#' @param interim_struct A optional list of parameters to control an interim assessment
#'  with an example structure below, where Beta=a positive value that is inversely proportional to risk,
#'  MA_years= the number of years to average index observations of when calculating deviations,
#'  assess_freq=the number of years between full assessments during with an interim assessment will happen
#'  every year, and Index_weights is a vector of length n indexes that weights all indexes for multi index
#'  inference. interim_struct<-list(Beta=1,MA_years=3,assess_freq=5,Index_weights=rep(1,max(ref_index[,3])))
#' @param run_parallel Option to use parallel processing on iterations.
#'  Defaults to FALSE
#' @param n_cores how many cores to use if running in parallel defaults to
#'  n_cores available - 1 (also capped at one less than the number of cores
#'  available)
#' @template verbose
#' @author Kathryn Doering & Nathan Vaughan
#' @examples
#' \dontrun{
#' # Create a temporary folder for the output and set the working directory:
#' temp_path <- file.path(tempdir(), "run_SSMSE_scen-example")
#' dir.create(temp_path, showWarnings = FALSE)
#'
#' # run 2 iteration and 1 scenario of SSMSE
#' run_SSMSE_scen(
#'   scen_name = "no_catch",
#'   iter = 1:2,
#'   OM_name = "cod",
#'   MS = "no_catch",
#'   out_dir_scen = temp_path,
#'   nyrs = 6,
#'   nyrs_assess = 3
#' )
#' unlink(temp_path, recursive = TRUE)
#' }
#'
run_SSMSE_scen <- function(scen_name = "scen_1",
                           out_dir_scen = NULL,
                           iter = 2,
                           OM_name = "cod",
                           OM_in_dir = NULL,
                           EM_name = NULL,
                           EM_in_dir = NULL,
                           run_EM_last_yr = FALSE,
                           MS = "no_catch",
                           use_SS_boot = TRUE,
                           nyrs = 100,
                           nyrs_assess = 3,
                           rec_devs_scen = NULL,
                           impl_error = NULL,
                           scen_seed = NULL,
                           sample_struct = NULL,
                           future_om_list = NULL,
                           sample_struct_hist = NULL,
                           interim_struct = NULL,
                           verbose = FALSE,
                           run_parallel = FALSE,
                           n_cores = NULL) {
  # input checks
  assertive.types::assert_is_a_string(scen_name)
  assertive.properties::assert_is_atomic(iter)
  assertive.types::assert_is_any_of(iter, classes = c("numeric", "integer"))
  if (!is.null(OM_name)) assertive.types::assert_is_a_string(OM_name)
  assertive.types::assert_is_a_bool(use_SS_boot)
  if (!is.null(EM_name)) assertive.types::assert_is_a_string(EM_name)
  if (!is.null(EM_in_dir)) assertive.types::assert_is_a_string(EM_in_dir)
  assertive.types::assert_is_a_string(MS)
  if (!is.null(out_dir_scen)) assertive.types::assert_is_a_string(out_dir_scen)
  assertive.types::assert_is_any_of(nyrs, classes = c("numeric", "integer"))
  assertive.properties::assert_is_of_length(nyrs, 1)
  assertive.types::assert_is_any_of(nyrs_assess, classes = c("numeric", "integer"))
  assertive.properties::assert_is_of_length(nyrs_assess, 1)
  if (!is.null(sample_struct)) assertive.types::assert_is_list(sample_struct)
  if (!is.null(sample_struct_hist)) assertive.types::assert_is_list(sample_struct_hist)
  assertive.types::assert_is_a_bool(verbose)

  # create the out_dir to store all files for all iter in the scenario.
  if (is.null(out_dir_scen)) {
    out_dir_iter <- scen_name
  } else {
    out_dir_iter <- file.path(out_dir_scen, scen_name)
  }
  dir.create(out_dir_iter, showWarnings = FALSE)
  # find the first iteration, in case other iterations have been run previously
  # for this scenario.
  all_folds <- list.dirs(path = out_dir_iter, full.names = FALSE)
  existing_iters <- grep("^[0-9]+$", all_folds, value = TRUE)
  if (length(existing_iters) > 0) {
    max_prev_iter <- max(as.integer(existing_iters))
    message(
      "Previous iterations found in folder for scenario ", scen_name, ".",
      " First iteration folder will be ", max_prev_iter + 1, "."
    )
  } else {
    max_prev_iter <- 0
  }

  # make a dataframe to store dataframes that error
  return_val <- vector(mode = "list", length = iter)
  if (run_parallel) {
    return_val <- foreach::`%dopar%`(
      foreach::foreach(i = seq_len(iter), .errorhandling = "pass"),
      {
        iter_seed <- vector(mode = "list", length = 3)
        names(iter_seed) <- c("global", "scenario", "iter")
        iter_seed[["global"]] <- scen_seed[["global"]]
        iter_seed[["scenario"]] <- scen_seed[["scenario"]]
        iter_seed[["iter"]] <- scen_seed[["iter"]][i]
        run_SSMSE_iter(
          out_dir = out_dir_iter,
          OM_name = OM_name,
          OM_in_dir = OM_in_dir,
          EM_name = EM_name,
          EM_in_dir = EM_in_dir,
          MS = MS,
          use_SS_boot = use_SS_boot,
          nyrs = nyrs,
          nyrs_assess = nyrs_assess,
          rec_dev_iter = rec_devs_scen[[i]],
          impl_error = impl_error[[i]],
          niter = max_prev_iter + i,
          run_EM_last_yr = run_EM_last_yr,
          iter_seed = iter_seed,
          sample_struct = sample_struct,
          future_om_list = future_om_list,
          sample_struct_hist = sample_struct_hist,
          interim_struct = interim_struct,
          verbose = verbose
        )
      }
    )
  } else {
    for (i in seq_len(iter)) {
      # for(i in seq_len(iter)){
      iter_seed <- vector(mode = "list", length = 3)
      names(iter_seed) <- c("global", "scenario", "iter")
      iter_seed[["global"]] <- scen_seed[["global"]]
      iter_seed[["scenario"]] <- scen_seed[["scenario"]]
      iter_seed[["iter"]] <- scen_seed[["iter"]][i]
      return_val[[i]] <- tryCatch(run_SSMSE_iter(
        out_dir = out_dir_iter,
        OM_name = OM_name,
        OM_in_dir = OM_in_dir,
        EM_name = EM_name,
        EM_in_dir = EM_in_dir,
        MS = MS,
        use_SS_boot = use_SS_boot,
        nyrs = nyrs,
        nyrs_assess = nyrs_assess,
        rec_dev_iter = rec_devs_scen[[i]],
        impl_error = impl_error[[i]],
        niter = max_prev_iter + i,
        run_EM_last_yr = run_EM_last_yr,
        iter_seed = iter_seed,
        sample_struct = sample_struct,
        future_om_list = future_om_list,
        sample_struct_hist = sample_struct_hist,
        interim_struct = interim_struct,
        verbose = verbose
      ), error = function(e) e)
    }
  }
  # summarize the errors from runs.
  run_failed_df <- NULL
  for (i in seq_len(iter)) {
    if ("error" %in% class(return_val[[i]])) {
      message(
        "Iteration ", max_prev_iter + i, " failed in directory ",
        out_dir_iter,
        ". Please delete folders with failed runs before running summary functions."
      )
      tmp_df <- data.frame(
        iteration = max_prev_iter + i,
        scenario = basename(out_dir_iter),
        out_dir = out_dir_iter,
        error = paste(return_val[[i]][["message"]])
      )
      # todo: add more info on why the run failed.
      run_failed_df <- rbind(run_failed_df, tmp_df)
    }
  }
  if (is.null(run_failed_df)) {
    run_failed_df <- "No errored iterations"
  }
  message("Completed all iterations for scenario ", scen_name)
  invisible(run_failed_df)
}

#' Run one iteration of an MSE using SS OM
#'
#' High level function to run 1 iteration of a scenario for a management
#' strategy evaluation using Stock Synthesis as the Operating model.
#' @param out_dir The directory to which to write output. IF NULL, will default
#'   to the working directory.
#' @param OM_name Name of a valid Stock Synthesis stock assessment model from
#'   which to create the OM. Currently, valid inputs are: \code{"cod"} or NULL,
#'   if using a custom model with path specified in \code{OM_in_dir}.
#' @param OM_in_dir Relative or absolute path to the operating model, if using a
#'   model outside of the SSMSE package.
#' @template MS
#' @param EM_name Should be NULL unless \code{MS = "EM"}. Name of a valid Stock
#'   Synthesis stock assessment model to use as an EM. If the value of EM_name
#'   is NULL and  \code{MS = "EM"}, then SSMSE will look for the estimation model
#'   in the path specified in EM_in_dir. valid inputs for EM_name are: \code{"cod"}
#'    or \code{NULL}.
#' @param EM_in_dir Relative or absolute path to the estimation model, if using a
#'   model outside of the SSMSE package. Note that this value should be NULL if
#'   \code{MS} has a value other than "EM".
#' @param run_EM_last_yr Should the EM run in the last year if the last year
#'  is an assessment year? TRUE/FALSE option, so the same for all scenarios and
#'  iterations. Defaults to FALSE.
#' @param use_SS_boot Should a bootstrapped data set generated by SS be used?
#'   Defaults to TRUE.
#' @param nyrs Number of years beyond the years included in the OM to run the
#'   MSE. A single integer value.
#' @param nyrs_assess The number of years between assessments. E.g., if an
#'   assessment is conducted every 3 years, put 3 here. A single integer value.
#'   (NOTE: we could make this more flexible by instead reading in a vector of
#'   assessment years, so users could specify irregular numbers of yrs between
#'   assessments.)
#' @param rec_dev_iter A recruitment deviation vector for the iteration.
#'  Dimensions are nyrs_assess\*number of fleets \* number of seasons
#' @param impl_error An implementation error vector for the iteration.
#'  Dimensions are nyrs_assess\*number of fleets \* number of seasons
#' @param niter The iteration number, which is also the name of the folder the
#'  results will be written to.
#' @param iter_seed List containing fixed seeds for this iteration.
#' @param sample_struct A optional list including which years, seasons, and fleets
#'  should be  added from the OM into the EM for different types of data.
#'  If NULL, the data structure will try to be infered from the pattern found
#'  for each of the datatypes within the EM datafiles. Include this strucutre
#'  for the number of years to extend the model out. Note that the data should
#'  be specified using the list component names and column names as in would be
#'  used in \code{r4ss::SS_readdat()}. The run_SSMSE_iter function examples
#'  give an example of what this structure should be. Running the function
#'  create_sample_struct() will also produce a sample_struct object in the
#'  correct form. Can be NULL only when MS is not EM.
#' @param future_om_list An optional list of lists including changes that should
#'  be made after the end year of the input model. Each first level list element
#'  outlines 1 change to be made to the operating model. To see an example, try
#'  running \code{\link{create_future_om_list}}. Defaults to NULL, which implies
#'  that the model will be extended forward in time assuming the original model
#'  structure.
#' @param sample_struct_hist An optional list including which years should be
#'  sampled for the historical period for the data generated from the OM. If 
#'  this is left as NULL, then the same sampling scheme will be used as in the
#'  OM's data file. If it is not NULL, then each year 
#' @param interim_struct A optional list of parameters to control an interim
#'  assessment with an example structure below, where Beta=a positive value
#'  that is inversely proportional to risk, MA_years= the number of years to
#'  average index observations of when calculating deviations, assess_freq=the
#'  number of years between full assessments during with an interim assessment
#'  will happen every year, and Index_weights is a vector of length n indexes
#'  that weights all indexes for multi index inference.
#'  interim_struct<-list(Beta = 1,
#'                       MA_years = 3,
#'                       assess_freq = 5,
#'                       Index_weights = rep(1, max(ref_index[, 3])))
#' @template verbose
#' @author Kathryn Doering & Nathan Vaughan
#' @examples
#' \dontrun{
#' # Create a temporary folder for the output
#' temp_path <- file.path(tempdir(), "run_SSMSE_iter-ex")
#' dir.create(temp_path)
#'
#' # run 1 iteration and 1 scenario of SSMSE
#' run_SSMSE_iter(
#'   OM_name = "cod",
#'   MS = "no_catch",
#'   out_dir = temp_path,
#'   nyrs = 6,
#'   nyrs_assess = 3
#' )
#' unlink(file.path(temp_path, "1"), recursive = TRUE)
#' # run 1 iteration and 1 scenario of SSMSE using an EM.
#' run_SSMSE_iter(
#'   OM_name = "cod",
#'   MS = "EM",
#'   out_dir = temp_path,
#'   EM_name = "cod",
#'   nyrs = 6,
#'   nyrs_assess = 3,
#'   sample_struct = list(
#'     catch = data.frame(Yr = 101:106, Seas = 1, FltSvy = 1, SE = 0.05),
#'     CPUE = data.frame(Yr = c(102, 105), Seas = 7, FltSvy = 2, SE = 0.01),
#'     lencomp = data.frame(
#'       Yr = c(102, 105), Seas = 1, FltSvy = 1,
#'       Sex = 0, Part = 0, Nsamp = 100
#'     ),
#'     agecomp = data.frame(
#'       Yr = c(102, 105), Seas = 1, FltSvy = 2,
#'       Sex = 0, Part = 0, Ageerr = 1,
#'       Lbin_lo = -1, Lbin_hi = -1, Nsamp = 50
#'     )
#'   )
#' )
#' unlink(temp_path, recursive = TRUE)
#' }
run_SSMSE_iter <- function(out_dir = NULL,
                           OM_name = "cod",
                           OM_in_dir = NULL,
                           EM_name = NULL,
                           EM_in_dir = NULL,
                           run_EM_last_yr = FALSE,
                           MS = "last_yr_catch",
                           use_SS_boot = TRUE,
                           nyrs = 100,
                           nyrs_assess = 3,
                           rec_dev_iter = NULL,
                           impl_error = NULL,
                           niter = 1,
                           iter_seed = NULL,
                           sample_struct = NULL,
                           future_om_list = NULL,
                           sample_struct_hist = NULL,
                           interim_struct = NULL,
                           verbose = FALSE) {
  # input checks ----
  # checks for out_dir, OM_name, OM_in_dir, EM_name, EM_in_dir done in create_out_dirs
  assertive.types::assert_is_a_bool(use_SS_boot)
  assertive.types::assert_is_a_string(MS)
  assertive.types::assert_is_any_of(nyrs, c("integer", "numeric"))
  assertive.types::assert_is_any_of(nyrs_assess, c("integer", "numeric"))
  assertive.types::assert_is_any_of(niter, c("integer", "numeric"))
  if (!is.null(sample_struct)) {
    assertive.types::assert_is_list(sample_struct)
    check_sample_struct(sample_struct)
  } else {
    if (MS == "EM") {
      stop(
        "sample_struct cannot be NULL when using an EM. Please specify. The ",
        "helper function create_sample_struct can be used to specify."
      )
    }
  }
  assertive.types::assert_is_a_bool(verbose)

  message("Starting iteration ", niter, ".")

  set.seed((iter_seed[["iter"]][1] + 123))
  # get and create directories, copy model files ----
  # assign or reassign OM_dir and OM_in_dir in case they weren't specified
  # as inputs
  out_loc <- create_out_dirs(
    out_dir = out_dir, niter = niter, OM_name = OM_name,
    OM_in_dir = OM_in_dir, MS = MS,
    EM_name = EM_name, EM_in_dir = EM_in_dir
  )
  if (is.null(out_loc)) { # out loc returns false if the iteration was already run.
    return(FALSE)
  }
  OM_out_dir <- out_loc[["OM_out_dir"]]
  OM_in_dir <- out_loc[["OM_in_dir"]]
  EM_in_dir <- out_loc[["EM_in_dir"]]
  EM_out_dir <- out_loc[["EM_out_dir"]]
  if (!is.null(EM_out_dir)) {
    EM_out_dir_basename <- strsplit(EM_out_dir, "_init$")[[1]][1]
  } else {
    EM_out_dir_basename <- NULL
  }
  copy_model_files(
    OM_in_dir = OM_in_dir, OM_out_dir = OM_out_dir,
    EM_in_dir = EM_in_dir, EM_out_dir = EM_out_dir
  )
  # clean model files ----
  # want to do this as soon as possible to "fail fast"
  # for now, this just gets rid of -year value observations, but could do
  # other things.
  clean_init_mod_files(
    OM_out_dir = OM_out_dir, EM_out_dir = EM_out_dir,
    overwrite = TRUE
  )

  # convert sample_struct names ----
  # get the full sampling structure for components that the user didnt specify.
  # if meaning is ambiguous, then this will exit on error.
  if (!is.null(sample_struct)) {
    sample_struct <- get_full_sample_struct(
      sample_struct = sample_struct,
      OM_out_dir = OM_out_dir
    )
    # convert to r4ss names
    sample_struct <- convert_to_r4ss_names(sample_struct)
    sample_struct_hist <- convert_to_r4ss_names(sample_struct_hist)
  }
  # MSE first iteration ----
  # turn the stock assessment model into an OM
  # This function is now needed in order to make changes such as run from
  # the par file and potentially change F method to 2 to unify results.

  create_OM(
    OM_out_dir = OM_out_dir, overwrite = TRUE,
    sample_struct_hist = sample_struct_hist, verbose = verbose, writedat = TRUE,
    nyrs_assess = nyrs_assess, rec_devs = rec_dev_iter,
    seed = (iter_seed[["iter"]][1] + 1234)
  )

  # Complete the OM run so it can be use for expect values or bootstrap
  if (use_SS_boot == TRUE) {
    OM_dat <- run_OM(
      OM_dir = OM_out_dir, boot = use_SS_boot, nboot = 1,
      verbose = verbose, init_run = TRUE, seed = (iter_seed[["iter"]][1] + 12345)
    )
  }
  if (use_SS_boot == FALSE) {
    stop(
      "Currently, only sampling can be done using the bootstrapping ",
      "capabilities within SS"
    )
    # TODO: add sampling functions then run a future sampling function that would
    # make it into a dataset.
  }
  message(
    "Finished running and sampling OM for the historical period for ",
    "iteration ", niter, "."
  )
  # get catch/discard using the chosen management strategy ----
  # This can use an estimation model or EM proxy, or just be a simple management
  # strategy


  new_catch_list <- parse_MS(
    MS = MS, EM_out_dir = EM_out_dir, init_loop = TRUE,
    OM_dat = OM_dat, OM_out_dir = OM_out_dir,
    verbose = verbose, nyrs_assess = nyrs_assess,
    interim_struct = interim_struct, dat_yrs = NA,
    seed = (iter_seed[["iter"]][1] + 123456)
  )

  message(
    "Finished getting catch (years ",
    (OM_dat[["endyr"]] + 1), " to ", (OM_dat[["endyr"]] + nyrs_assess),
    ") to feed into OM for iteration ", niter, "."
  )

  # Next iterations of MSE procedure ----
  # set up all the years when the assessment will be done.
  # remove first value, because done in the intialization stage.
  styr_MSE <- OM_dat[["endyr"]]
  assess_yrs <- seq(styr_MSE, styr_MSE + nyrs, nyrs_assess)
  assess_yrs <- assess_yrs[-1]
  # calculate years after the last assessment. The OM will need to run
  # 1 additional time if this is the case.
  extra_yrs <- nyrs - (assess_yrs[length(assess_yrs)] - styr_MSE)
  if (extra_yrs == 0) {
    test_run_EM_yr <- assess_yrs[length(assess_yrs)]
  } else {
    test_run_EM_yr <- NULL
  }
  # Loop over the assessment years.
  for (yr in assess_yrs) {
    # checks, esp. to make sure future catch is not larger than the population
    # biomass (or size, depending on units)
    # TODO: improve this function below; we need better checks for all the
    # new_catch_list components
    # check_future_catch(catch = new_catch_list[["catch_bio"]],
    #                    OM_dir = OM_out_dir,
    #                    catch_units = "bio")
    if (verbose) {
      message(
        "Extending, running, and sampling from the OM though year ", yr,
        "."
      )
    }
    rec_devs_chunk <- rec_dev_iter[1:nyrs_assess]
    rec_dev_iter <- rec_dev_iter[-(1:nyrs_assess)]
    impl_error_chunk <- impl_error[1:(nyrs_assess * OM_dat[["nseas"]] * OM_dat[["Nfleet"]])]
    impl_error <- impl_error[-(1:(nyrs_assess * OM_dat[["nseas"]] * OM_dat[["Nfleet"]]))]

    extend_OM(
      catch = new_catch_list[["catch"]],
      discards = new_catch_list[["discards"]],
      harvest_rate = new_catch_list[["catch_F"]],
      OM_dir = OM_out_dir,
      sample_struct = sample_struct,
      nyrs_extend = nyrs_assess,
      rec_devs = rec_devs_chunk,
      impl_error = impl_error_chunk,
      verbose = verbose,
      seed = (iter_seed[["iter"]][1] + 234567 + yr)
    )
    # rerun OM (without estimation), get samples (or expected values)
    if (use_SS_boot == TRUE) {
      if (!is.null(interim_struct)) {
        if (MS == "Interim" & !is.null(interim_struct[["auto_corr"]])) {
          new_OM_dat <- run_OM(
            OM_dir = OM_out_dir, boot = FALSE, nboot = 1,
            verbose = verbose, seed = (iter_seed[["iter"]][1] + 345678 + yr)
          )
        } else {
          new_OM_dat <- run_OM(
            OM_dir = OM_out_dir, boot = use_SS_boot, nboot = 1,
            verbose = verbose, seed = (iter_seed[["iter"]][1] + 345678 + yr)
          )
        }
      } else {
        new_OM_dat <- run_OM(
          OM_dir = OM_out_dir, boot = use_SS_boot, nboot = 1,
          verbose = verbose, seed = (iter_seed[["iter"]][1] + 345678 + yr)
        )
      }
    } else {
      stop(
        "Currently, only sampling can be done using the bootstrapping ",
        "capabilities within SS"
      )
    }
    message(
      "Finished running and sampling OM through year ", new_OM_dat[["endyr"]],
      "."
    )
    if (run_EM_last_yr == FALSE && isTRUE(yr == test_run_EM_yr)) {
      skip_EM_run <- TRUE
    } else {
      skip_EM_run <- FALSE
    }
    if (skip_EM_run == FALSE) {
      # if using an EM, want to save results to a new folder
      if (!is.null(EM_out_dir) & MS != "Interim") {
        new_EM_out_dir <- paste0(EM_out_dir_basename, "_", yr)
        dir.create(new_EM_out_dir)
        success <- copy_model_files(
          EM_in_dir = EM_out_dir,
          EM_out_dir = new_EM_out_dir
        )
        EM_out_dir <- new_EM_out_dir
      }
      # Only want data for the new years: (yr+nyrs_assess):yr
      # create the new dataset to input into the EM
      # loop EM and get management quantities.
      if (!is.null(EM_out_dir_basename)) {
        tmp_EM_init_dir <- paste0(EM_out_dir_basename, "_init")
      } else {
        tmp_EM_init_dir <- NULL
      }
      new_catch_list <- parse_MS(
        MS = MS,
        EM_out_dir = EM_out_dir,
        EM_init_dir = tmp_EM_init_dir,
        OM_dat = new_OM_dat,
        init_loop = FALSE, verbose = verbose,
        nyrs_assess = nyrs_assess,
        OM_out_dir = OM_out_dir,
        dat_yrs = (yr + 1):(yr + nyrs_assess),
        sample_struct = sample_struct,
        interim_struct = interim_struct,
        seed = (iter_seed[["iter"]][1] + 5678901 + yr)
      )
      message(
        "Finished getting catch (years ", (yr + 1), " to ",
        (yr + nyrs_assess), ") to feed into OM for iteration ", niter, "."
      )
    }
  }
  if (extra_yrs > 0) {
    message("Running the OM 1 final time, because last year extends past the last 
    assessment.")
    yr <- assess_yrs[length(assess_yrs)] + extra_yrs
    # get recdevs, impl_error
    rec_devs_chunk <- rec_dev_iter[1:extra_yrs]
    rec_dev_iter <- rec_dev_iter[-(1:extra_yrs)]
    impl_error_chunk <- impl_error[1:(extra_yrs * OM_dat[["nseas"]] * OM_dat[["Nfleet"]])]
    impl_error <- impl_error[-(1:(extra_yrs * OM_dat[["nseas"]] * OM_dat[["Nfleet"]]))]
    # sanity checks
    assertive.properties::assert_is_of_length(rec_dev_iter, 0)
    assertive.properties::assert_is_of_length(impl_error, 0)
    subset_catch_list <- lapply(new_catch_list,
      function(x, yr) new_catch <- x[x[["year"]] <= yr, ],
      yr = yr
    )
    extend_OM(
      catch = subset_catch_list[["catch"]],
      discards = subset_catch_list[["discards"]],
      harvest_rate = subset_catch_list[["catch_F"]],
      OM_dir = OM_out_dir,
      sample_struct = sample_struct,
      nyrs_extend = extra_yrs,
      rec_devs = rec_devs_chunk,
      impl_error = impl_error_chunk,
      verbose = verbose,
      seed = (iter_seed[["iter"]][1] + 6789012)
    )
    # Don't need bootstrapping, b/c not samplling
    run_OM(
      OM_dir = OM_out_dir, boot = FALSE, verbose = verbose,
      seed = (iter_seed[["iter"]][1] + 7890123)
    )
  }
  message("Finished iteration ", niter, ".")
  invisible(TRUE)
}
